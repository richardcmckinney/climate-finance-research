# Clean subset similar to manuscript logic
data_clean <- subset(data, Status == "IP Address" & suppressWarnings(as.numeric(Progress)) >= 10)

market_count <- sum(grepl("market", data$Q3.11, ignore.case = TRUE), na.rm = TRUE); total_responses <- sum(!is.na(data$Q3.11)); print(prop.test(market_count, total_responses, conf.level = 0.95))
